/*----------------------------------------
 *  fw_pin_init
 *----------------------------------------*/
inline fw_pin_t fw_pin_init(uint16_t port, uint16_t pin){
  fw_pin_t result = {
    .group.port = port,
    .group.pin = pin,
  };
  return result;
}



